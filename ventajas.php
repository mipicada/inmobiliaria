<h2>¿Qué ventajas me ofrece esta web respecto a la actual?</h2>
<p>Para hacerlo de una forma ordenada y no cargarte de información, vamos a ir por partes.</p>
<h3>Aspectos técnicos</h3>
<p>Si te fijas en el <a href="view-source:http://www.i7islas.es/inicio" target="_blank">código de la página principal de la actual web,</a> puedes ver que acumula 2.217 líneas de código. Si ahora ves <a href="view-source:http://www.empresas.interneus.com/7islas" target="_blank">el código de esta web,</a> comprobarás que se ha reducido en más de la mitad la cantidad de código.</p>
<p><strong>¿A qué afecta esto?.</strong> Directamente a la carga de la web. Por un lado reordenamos el código, situando los elementos esenciales al principio del documento y los más pesados al final, de forma que hace una carga estructurada de la página. Por otro optimizamos el código, quitando todo lo obsoleto y reduciendolo a lo necesario.</p> 
<p>Por último se programa la web para que se almacene en el caché del navegador (de forma que si el usuario vuelve a entrar a tu web no tiene que volver a cargar todos los elementos y la carga es casi instantánea) y se habilita para navegación optimizada en dispositivos móviles.</p>
<p>Igualmente se habilitan compresores de archivos (gzip) que afecta igualmente a la carga de archivos y se estructura la web siguiendos los estándares de posicionamiento y recomendaciones de <b style="font-style:italic">Google.</b></p>
<p>En general, se elabora la web optimizando todas las deficiencias detectadas en la auditoria siendo auditada nuevamente hasta conseguir un nivel alto, tanto en posicionamiento como en carga.</p>
<h3>Apariencia y adaptabilidad</h3>
<p>Te presentamos una web panorámica, que aprovecha todo el ancho de cualquier pantalla, mostrándose limpia, ordenada y desahogada.</p>
<p>Compatible con todos los navegadores web y móviles.</p>
<p>La mayoria de las páginas web están centradas y con unas dimensiones exactas, como es el caso de tu página actual. Esto tiene una ventaja para el diseñador, solo tiene que realizar un diseño cómodo. Pero ofrece ciertas desventajas al usuario. Tu web en una pantalla grande, se verá con unos márgenes desproporcionados, apareciendo todo el contenido "amontonado" dentro de un rectángulo en el centro. En pantallas inferiores a 1152 píxeles (la segunda resolución más usada en el mundo es de 1024 píxeles de ancho por 768 de alto,por ejemplo en netbooks, pantallas no panorámicas o portatiles pequeños), para poder ver tu página tendrás que usar las barras de desplazamiento horizontal, lo que, desde el punto de vista de tus clientes, se convierte en un incoveniente y algo incómodo e incluso molesto. Puedes hacer la prueba modificando la resolución de tu pc.</p>
<p>Esta página se adaptará perfectamente a cada resolución, lo cual no quiere decir que se vea exactamente igual, sino que programamos la web para que, en función del tamaño de la pantalla, los "módulos" se adapten para ofrecer a tus usuarios una experiencia sencilla y agradable, destacando siempre los aspectos más importantes, y sin tener que desplazar la barra de desplazamiento horizontal para poder ver todo el contenido.</p>
<p>Facilitar la navegación a tus usuarios es básico, ya que en función de lo cómodo que se sientan en tu página y las facilidades de accesibilidad que tengan permanecerán más tiempo en tu web, con lo que las posibilidades de convertirlo en cliente crecen.</p>
<p>Por ello tus usuarios podrán acceder a tu web desde dispositivos móviles, teniendo una navegación y visualización de contenidos totalmente adaptada al smartphone o tablet desde el que se conecte. En el caso de tu negocio, ofrecer esta posibilidad a tus usuarios es vital para incrementar o fidelizar a tus clientes, ya que no necesitan estar en un pc de sobremesa o portatil para poder acceder a todos los contenidos de tu web con un nivel de visualización y navegación de plenas garantias.</p>
<h3>Navegación</h3>
<p>Estudiando detenida y detalladamente tu antigua web hemos detectado ciertas posibles mejoras, algunas de ellas expuestas en el informe.</p>
<p>Por un lado, quitamos el que se abra una pestaña nueva en el navegador cada vez que cambias de página. Mantenemos los menús y buscador en todas y cada una de las páginas para que el usuario pueda navegar facilmente sin tener que estar retrocediendo o volver al inicio. Como puedes ver en esta misma página, el buscador siempre se muestra a tu izquierda, por lo que puedes hacer continuas búsquedas sin tener que abandonar la página en la que te encuentras ni que te abra otra pestaña el navegador.</p>
<p>El hecho de mantener siempre a la vista el menú de navegación (inicio, vender, comprar, ofertas, nosotros, etc) y tener señalizado claramente en qué "sección" de la web se encuentra, transmite a tus usuarios la sensación de comodidad y facilidad necesaria para que no deseen abandonar tu web para entrar a otra.</p>
<p><strong>En las imágenes</strong> del inicio, puedes comprobar que, al hacer click sobre cada una de ellas, se abre una pequeña ventana con la imagen ampliada y una breve información sobre el inmueble. Desde aquí el usuario podrá acceder directamente a la ficha completa del inmueble, donde se mostrarán todas las imágenes, videos y detalle del inmueble, o ir al <b style="font-style:italic">¿Te interesa este inmueble?"</b>, que le llevará a un breve formulario de contacto, que recibirás en tu correo con el número de referencia del inmueble en cuestión.</p>
<p>En la ficha del inmueble tendrá la misma opción de contacto, pero también le ofreceremos una opción que puede ser muy interesante por lo fácil y rápido que puede resultar para el usuario que no quiera llamar por teléfono: un pequeño chat vía email. Es decir, tendrá una opción parecida al tipico "déja tu comentario" pero en plan "¿tienes alguna consulta?, te respondemos online", y lo que hará será enviar un email con la consulta del cliente, y al que podrás responder desde tu propio email al del usuario. Si asignas una cuenta de email (no visible para el usuario) a esta función, algo así como "consultasweb@i7islas.es" y que dicha cuenta la tengas en tu móvil como preferente, o asignada a alguien de la oficina para atender esas consultas de inmediato, puedes ofrecer un servicio muy atractivo, ya que para cualquier cliente obtener información tan cómoda y rapidamente es un valor añadido, y especialmente a usuarios que lo hagan desde sus dispositivos móviles, ya que se ha de suponer que estarán moviéndose buscando inmuebles, y tener ese servicio les facilitará mucho las cosas.</p>
<p><strong>El nº de teléfono.</strong> Al contrario que lo expuesto anteriormente, hay mucha gente que lo tienen que hacer todo por teléfono, y para ellos es bueno que tengan siempre visible y accesible el número de teléfono, sin tener que entrar a otra página para poder ver los datos de contacto.</p>
<p>Se ofrece también a tus usuarios la posibilidad de, desde teléfonos móviles, al pulsar sobre el número de teléfono en la web, se haga directamente la llamada, evitando tener que salir de la página para ir al teléfono y marcar.</p>
<p>Igual que aparece en primera plana el teléfono, desaparece por completo cualquier dirección de correo electrónico, ya que el cliente podrá contactar directamente desde la web y evitamos así cualquier posible spam que puedan hacer, Hay robots de búsqueda que se dedican a rastrear las cuentas de correo en las web para hacer spam.</p>
<p>Una novedad importante es ofrecer a los usuarios la posibilidad de compartir las viviendas en redes sociales, de forma que todos sus contactos puedan verlas y acceder directamente a la ficha de la vivienda en tu web.</p>
<h3>Otros aspectos</h3>
<p>Verás que faltan algunas cosas importantes, como los distintos idiomas, pero no te preocupes, simplemente no lo hemos puesto a conciencia ya que esto es solo un boceto practicamente sin programación.</p>
<p><strong>El mapa.</strong> Hemos omitido a conciencia el mapa que tienes en tu actual web con cada provincia. Ponerlo no será ningún problema, pero quiero explicarte el porqué lo omitimos, aunque como te digo, es decisión tuya dejarlo.</p>
<p>Ese mapa por un lado es una de las mayores cargas de la página, que la ralentizará siempre. No tiene más que una función visual, ya que lleva a un buscador que, simplemente, te introduce la provincia en la que has marcado, pero reitera lo que ya tienes en el buscador, y entendemos que es cargar innecesariamente la web de peso.</p>
<p><strong>Web móvil.</strong> Esta web, como expuse anteriormente, está totalmente adaptada a dispositivos móviles, de cualquier navegador y resolución, pero teniendo en cuenta que más que una web es un catálogo, habría que tener en cuenta la posibilidad de preparar una versión especial para móviles.</p>
<p>La diferencia es la siguiente. Con esta versión, en móviles se verá exactamente la misma página (puedes hacer la prueba) adaptada al tamaño del dispositivo. Con una versión especial para dispositivos móviles es una página independiente, con el mismo contenido, apariencia, etc, pero optimizada 100% para móviles. El tamaño de las imágenes (y esta web tendrá muchas) tendrá el peso adecuado para que la carga sea más rápida y estará estructurada en cuanto a datos se refiere de una forma distinta. Al entrar tus clientes a tu web desde un smartphone o tablet, automaticamente se les redirigirá a esta versión móvil.</p>
<p><strong>Ventajas:</strong> Está 100% optimizada y especialmente lo notarás en la carga de la web.</p>
<p><strong>Desventajas:</strong> El precio es superior, y al introducir imágenes tendrás que hacerlo en dos tamaños distintos, una para la versión "clásica" y otra para la versión "móvil".</p>
<h3>Gestor de contenidos</h3>
<p>Evidentemente esta web va acompañada de un gestor de contenidos para poder introducir archivos multimedia (videos, imágenes), contenido, etc.</p>
<p>Nosotros lo hacemos y programamos desde 0 para adaptarlo totalmente al nuevo entorno, pero se os puede facilitar el trabajo adaptándolo al que teneis actualmente para que el cambio sea menos "traumático".</p>
<p>Opcionalmente, podemos incluir un gestor de contenidos en versión para móviles para poder incluir contenido e imágenes directamente desde el móvil o tablet.</p>
<p>En principio el gestor de contenidos tendría dos tipos de usuarios. Uno administrador, con acceso total, y otro para incluir o modificar contenidos.</p>
<h3>Conclusiones</h3>
<p>Como dijimos antes esto es solo un boceto, totalmente modificable y adaptable a lo que necesites, por lo que está abierto a cualquier modificación y adaptabilidad.</p>
